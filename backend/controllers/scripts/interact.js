import ethers from 'ethers';

// Provider
const alchemyProvider = new ethers.providers.JsonRpcProvider(API_URL);

// Signer
const signer = new ethers.Wallet(PRIVATE_KEY, alchemyProvider);

// Contract
const helloWorldContract = new ethers.Contract(CONTRACT_ADDRESS, ABI, signer)

async function main() {
    const employeeAddress = '0xFe781e61A25c9ab0D93bbC251f11Aa44d3dAA3A3';
    const transferAmount = 2 * 10 ** 12;

    try {
        
        const tx = await helloWorldContract.rewardEmployee(employeeAddress, transferAmount);
        await tx.wait();
    
         console.log(`Successfully transferred ${transferAmount} TRT to ${employeeAddress}`);
         return `Successfully transferred ${transferAmount} TRT to ${employeeAddress}`;
    } catch (error) {
        console.error('Error transferring tokens:', error);
    }

    
}

export default main;